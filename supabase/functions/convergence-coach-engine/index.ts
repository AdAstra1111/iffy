function check2A_EntityPresenceConsistency(
  entities: any[],
  entityLinks: any[]
): FeasibilityFinding[] {
  const findings: FeasibilityFinding[] = [];

  for (const entity of entities) {
    if (entity.entity_type !== "character") continue;
    const actualCount = entityLinks.filter(l => l.entity_id === entity.id).length;
    if (entity.scene_count !== actualCount) {
      findings.push({
        checkId: "2A-entity-presence",
        severity: "medium",
        entityOrSystem: entity.canonical_name,
        description: `narrative_entities.scene_count=${entity.scene_count}, actual links=${actualCount}`,
        expected: String(actualCount),
        actual: String(entity.scene_count),
      });
    }
  }
  return findings;
}

