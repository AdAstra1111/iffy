function check1A_BeatCountConsistency(
  storyOutline: any,
  beatSheet: any
): NarrativeStructureFinding | null {
  const storyBeats = storyOutline?.beats || storyOutline?.entries || [];
  const sheetBeats = beatSheet?.beats || beatSheet?.entries || [];
  const storyCount = storyBeats.length;
  const sheetCount = sheetBeats.length;
  const threshold = Math.round(storyCount * 0.8);

  if (sheetCount < threshold) {
    const missing = storyBeats
      .filter((sb: any) => !sheetBeats.some((bb: any) => (bb.name || bb.title) === (sb.name || sb.title)))
      .map((b: any) => `Beat ${b.number}: ${b.name || b.title}`);
    return {
      checkId: "1A-beat-count",
      severity: "high",
      upstreamDoc: "story_outline",
      downstreamDoc: "beat_sheet",
      description: `beat_sheet has ${sheetCount} beats, story_outline has ${storyCount}. Below 80% threshold (${threshold}). ${missing.length} story beats missing from beat_sheet.`,
      divergenceType: "omission",
      affectedElements: missing.slice(0, 5),
    };
  }
  return null;
}

function check1B_BeatNameConsistency(
  storyOutline: any,
  beatSheet: any
): NarrativeStructureFinding[] {
  const findings: NarrativeStructureFinding[] = [];
  const storyBeats = storyOutline?.beats || storyOutline?.entries || [];
  const sheetBeats = beatSheet?.beats || beatSheet?.entries || [];

  for (const sb of storyBeats) {
    const matchingSheetBeat = sheetBeats.find(
      (bb: any) => (bb.name || bb.title || "").toLowerCase() === (sb.name || sb.title || "").toLowerCase()
    );
    if (!matchingSheetBeat) continue;

    // Check if key entities from story outline beat description are missing in sheet beat
    const storyEntities = extractKeyEntities(sb.description);
    const sheetEntities = extractKeyEntities(matchingSheetBeat.description);
    const missing = storyEntities.filter(e => !sheetEntities.includes(e));

    if (missing.length > storyEntities.length * 0.5 && missing.length >= 2) {
      findings.push({
        checkId: "1B-beat-name",
        severity: "medium",
        upstreamDoc: "story_outline",
        downstreamDoc: "beat_sheet",
        description: `Beat "${sb.name}" in beat_sheet is missing key entities from story_outline: ${missing.join(", ")}`,
        divergenceType: "omission",
        affectedElements: missing,
      });
    }
  }
  return findings;
}

function check1C_ActStructurePreservation(
  treatment: any,
  beatSheet: any
): NarrativeStructureFinding[] {
  const findings: NarrativeStructureFinding[] = [];
  if (!treatment || !beatSheet) return findings;

  // Handle nested structure: { treatment: { treatment_narrative: "..." } }
  const treatmentObj = typeof treatment === 'object' && !Array.isArray(treatment)
    ? (treatment.treatment_narrative ? treatment.treatment_narrative : treatment)
    : treatment;

  // If treatment is a plain text string, use the whole text as a single section check
  if (typeof treatmentObj === 'string') {
    const treatmentText = treatmentObj;
    const treatmentEntities = extractKeyEntities(treatmentText).slice(0, 10);
    const sheetBeats = beatSheet.beats || beatSheet.entries || [];
    let foundInBeats = 0;
    for (const entity of treatmentEntities) {
      if ((sheetBeats as any[]).some((bb: any) => (bb.description || "").toUpperCase().includes(entity))) {
        foundInBeats++;
      }
    }
    if (foundInBeats < treatmentEntities.length * 0.3 && treatmentEntities.length >= 3) {
      findings.push({
        checkId: "1C-act-structure-full",
        severity: "medium",
        upstreamDoc: "treatment",
        downstreamDoc: "beat_sheet",
        description: `Treatment text has ${treatmentEntities.length} key entities, but only ${foundInBeats} appear in beat_sheet. Low cross-coverage may indicate structural drift.`,
        divergenceType: "omission",
        affectedElements: treatmentEntities.slice(0, 5),
      });
    }
    return findings;
  }

  // Structured act fields
  const actSections = [
    { key: "act_1", name: "Act 1" },
    { key: "act_2a", name: "Act 2A" },
    { key: "midpoint", name: "Midpoint" },
    { key: "act_2b", name: "Act 2B" },
    { key: "act_3", name: "Act 3" },
  ];

  for (const act of actSections) {
    const actText = treatmentObj[act.key];
    if (!actText) continue;

    const actEntities = extractKeyEntities(String(actText));
    if (actEntities.length === 0) continue;

    const sheetBeats = beatSheet.beats || beatSheet.entries || [];
    let foundInBeats = 0;
    for (const entity of actEntities.slice(0, 5)) {
      if ((sheetBeats as any[]).some((bb: any) => (bb.description || "").toUpperCase().includes(entity))) {
        foundInBeats++;
      }
    }

    if (foundInBeats === 0 && actEntities.length >= 2) {
      findings.push({
        checkId: `1C-act-structure-${act.key}`,
        severity: "high",
        upstreamDoc: "treatment",
        downstreamDoc: "beat_sheet",
        description: `${act.name} content in treatment has no corresponding representation in beat_sheet.`,
        divergenceType: "omission",
        affectedElements: actEntities.slice(0, 3),
      });
    }
  }
  return findings;
}

function check1D_CharacterArcConsistency(
  conceptBrief: any,
  beatSheet: any
): NarrativeStructureFinding[] {
  const findings: NarrativeStructureFinding[] = [];
  if (!conceptBrief || !beatSheet) return findings;

  // Extract character names from concept_brief.premise
  const premise = conceptBrief.premise || conceptBrief.logline || "";
  const cbCharacters = extractKeyEntities(premise).slice(0, 8);

  const sheetBeats = beatSheet.beats || beatSheet.entries || [];
  const sheetText = (sheetBeats as any[]).map((b: any) => b.description || b.title || "").join(" ").toUpperCase();

  for (const char of cbCharacters) {
    if (!sheetText.includes(char)) {
      findings.push({
        checkId: "1D-character-arc",
        severity: "medium",
        upstreamDoc: "concept_brief",
        downstreamDoc: "beat_sheet",
        description: `Character "${char}" appears in concept_brief premise but has no mention in beat_sheet.`,
        divergenceType: "omission",
        affectedElements: [char],
      });
    }
  }
  return findings;
}

function check1E_MidpointConvergence(
  treatment: any,
  beatSheet: any
): NarrativeStructureFinding | null {
  if (!treatment || !beatSheet) return null;

  // Handle nested structure: { treatment: { treatment_narrative: "..." } }
  let treatmentMidpoint: string = "";
  if (treatment.midpoint) {
    treatmentMidpoint = treatment.midpoint;
  } else if (treatment.treatment?.treatment_narrative) {
    // Plain text treatment — skip midpoint check (no structured midpoint)
    return null;
  }

  if (!treatmentMidpoint) return null;

  const treatmentEntities = extractKeyEntities(treatmentMidpoint);
  if (treatmentEntities.length === 0) return null;

  const sheetBeats = beatSheet.beats || beatSheet.entries || [];
  // Find midpoint beat: name contains "midpoint" or page range around 55-70
  const midpointBeat = sheetBeats.find((b: any) => {
    const nameMatch = b.name?.toLowerCase().includes("midpoint");
    const pageMatch = b.page_range && (() => {
      const page = parseInt(b.page_range.split("-")[0]);
      return page >= 55 && page <= 70;
    })();
    return nameMatch || pageMatch;
  });

  if (!midpointBeat) {
    return {
      checkId: "1E-midpoint",
      severity: "critical",
      upstreamDoc: "treatment",
      downstreamDoc: "beat_sheet",
      description: `treatment.midpoint is defined but beat_sheet has no midpoint beat.`,
      divergenceType: "omission",
      affectedElements: treatmentEntities.slice(0, 3),
    };
  }

  const beatEntities = extractKeyEntities(midpointBeat.description);
  const missing = treatmentEntities.filter(e => !beatEntities.includes(e));
  const contradictions = beatEntities.filter(e => !treatmentEntities.includes(e) && e.length > 3);

  if (missing.length > 0 || contradictions.length > 2) {
    return {
      checkId: "1E-midpoint",
      severity: "critical",
      upstreamDoc: "treatment",
      downstreamDoc: "beat_sheet",
      description: `beat_sheet midpoint beat "${midpointBeat.name}" diverges from treatment.midpoint. Missing: ${missing.join(", ")}. New: ${contradictions.join(", ")}.`,
      divergenceType: missing.length > contradictions.length ? "omission" : "contradiction",
      affectedElements: [...missing, ...contradictions].slice(0, 5),
    };
  }

  return null;
}

