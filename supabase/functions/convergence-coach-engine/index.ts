/**
 * check2B — entity population completeness check.
 * Fetches data via direct REST API to avoid supabase client field-mapping issues.
 * Computes orphaned link counts by checking if linked entity IDs exist in narrative_entities.
 */
async function check2B_viaREST(
  supabaseUrl: string,
  serviceRoleKey: string,
  projectId: string
): Promise<FeasibilityFinding[]> {
  const findings: FeasibilityFinding[] = [];

  try {
    const headers = {
      apikey: serviceRoleKey,
      Authorization: `Bearer ${serviceRoleKey}`,
      "Content-Type": "application/json",
    };

    // 1. Get all distinct entity_ids from links
    const linksRes = await fetch(
      `${supabaseUrl}/rest/v1/narrative_scene_entity_links?project_id=eq.${projectId}&select=entity_id`,
      { headers }
    );
    const linksJson = await linksRes.json();
    if (!Array.isArray(linksJson)) return findings;
    const allLinkedIds = [...new Set(linksJson.map((l: any) => l.entity_id).filter(Boolean))];
    if (allLinkedIds.length === 0) return findings;

    // 2. Get character entity IDs
    const charRes = await fetch(
      `${supabaseUrl}/rest/v1/narrative_entities?project_id=eq.${projectId}&entity_type=eq.character&select=id,canonical_name`,
      { headers }
    );
    const charData: Array<{ id: string; canonical_name: string }> = await charRes.json();

    // 3. Get location entity IDs
    const locRes = await fetch(
      `${supabaseUrl}/rest/v1/narrative_entities?project_id=eq.${projectId}&entity_type=eq.location&select=id`,
      { headers }
    );
    const locData: Array<{ id: string }> = await locRes.json();

    const charIdSet = new Set(charData.map(e => e.id));
    const locIdSet = new Set(locData.map(e => e.id));

    let orphanedCharCount = 0;
    for (const id of allLinkedIds) {
      if (!charIdSet.has(id) && !locIdSet.has(id)) {
        orphanedCharCount++;
      }
    }

    if (orphanedCharCount > 0) {
      findings.push({
        checkId: "2B-character-population",
        severity: orphanedCharCount > 30 ? "high" : "medium",
        entityOrSystem: "character population",
        description: `${orphanedCharCount} links in narrative_scene_entity_links reference entity IDs not in narrative_entities (orphaned — pre-dedup data or entity extraction gap)`,
        expected: "All links resolve to narrative_entities records",
        actual: `${orphanedCharCount} orphaned links`,
      });
    }

    // 4. Phantom characters: character entities with no links
    const linkedRes = await fetch(
      `${supabaseUrl}/rest/v1/narrative_scene_entity_links?project_id=eq.${projectId}&entity_id=in.(${charData.map(e => e.id).join(',')})&select=entity_id`,
      { headers }
    );
    const linkedJson: Array<{ entity_id: string }> = await linkedRes.json();
    const linkedCharIdSet = new Set(linkedJson.map(l => l.entity_id).filter(Boolean));
    const phantomChars = charData.filter(e => !linkedCharIdSet.has(e.id));

    if (phantomChars.length > 0) {
      findings.push({
        checkId: "2B-phantom-characters",
        severity: "low",
        entityOrSystem: "phantom characters",
        description: `${phantomChars.length} character entities in narrative_entities have no scene links`,
        expected: "All narrative character entities appear in scene links",
        actual: phantomChars.map(e => e.canonical_name).join(", ").slice(0, 80),
      });
    }

  } catch(err) {
    console.error("[check2B_viaREST] error:", String(err));
  }

  return findings;
}


function check2C_LocationFeasibility(
  sceneGraph: any
): FeasibilityFinding[] {
  const findings: FeasibilityFinding[] = [];
  if (!sceneGraph?.scenes?.length) return findings;

  const allLocations: string[] = [];
  for (const scene of sceneGraph.scenes) {
    // location field is a single string (from scene_graph_versions.location)
    const loc = (scene.location as string) || "";
    if (loc) {
      // Normalise: "VARIOUS LOCATIONS" stays as-is; "NEPALESE MOUNTAINS" stays
      allLocations.push(loc.toUpperCase());
    }
    // Fallback: parse from slugline
    if (!loc && scene.slugline) {
      const parts = scene.slugline.split(/\s*[.-]\s*/);
      if (parts.length >= 2) {
        allLocations.push(parts[1].replace(/^(INT|EXT|EST|INT\/EXT|I\/E)\.\s*/i, "").trim().toUpperCase());
      }
    }
  }

  if (allLocations.length === 0) return findings;

  const uniqueLocations = [...new Set(allLocations.map(l => l.toUpperCase()))];
  const locationCounts = countOccurrences(allLocations.map(l => l.toUpperCase()));
  const recurringLocations = Object.entries(locationCounts).filter(([, count]) => count >= 3).length;
  const oneOffLocations = Object.entries(locationCounts).filter(([, count]) => count === 1).length;

  let complexity: "low" | "medium" | "high" = "low";
  if (uniqueLocations.length > 15 || (uniqueLocations.length > 10 && oneOffLocations > recurringLocations)) {
    complexity = "high";
  } else if (uniqueLocations.length > 5) {
    complexity = "medium";
  }

  findings.push({
    checkId: "2C-location-feasibility",
    severity: complexity === "high" ? "high" : "low",
    entityOrSystem: "location_cost",
    description: `${uniqueLocations.length} unique locations, ${recurringLocations} recurring (≥3 uses), ${oneOffLocations} one-offs → production complexity: ${complexity}`,
    expected: complexity !== "high" ? "≤15 unique locations or ≥recurring ≥one-offs" : "high one-off location count — budget risk",
    actual: `complexity: ${complexity}`,
  });

  return findings;
}

function check2D_DayNightDistribution(
  sceneGraph: any
): FeasibilityFinding[] {
  const findings: FeasibilityFinding[] = [];
  if (!sceneGraph?.scenes?.length) return findings;

  const timeOfDayCounts: Record<string, number> = {};
  for (const scene of sceneGraph.scenes) {
    const tod = (scene.time_of_day as string) || "unknown";
    timeOfDayCounts[tod] = (timeOfDayCounts[tod] || 0) + 1;
  }

  const total = sceneGraph.scenes.length;
  const nightCount = (timeOfDayCounts["night"] || timeOfDayCounts["NIGHT"] || 0) +
    Object.entries(timeOfDayCounts)
      .filter(([k]) => k.toLowerCase().includes("night"))
      .reduce((acc, [, v]) => acc + v, 0);

  // Count more broadly
  let nightTotal = 0;
  for (const [key, count] of Object.entries(timeOfDayCounts)) {
    if (key.toLowerCase().includes("night") || key.toLowerCase() === "dark") {
      nightTotal += count;
    }
  }

  const nightRatio = nightTotal / total;

  if (nightRatio > 0.6) {
    findings.push({
      checkId: "2D-day-night",
      severity: "medium",
      entityOrSystem: "shoot_schedule",
      description: `${nightTotal}/${total} scenes (${Math.round(nightRatio * 100)}%) are night scenes — night-shoot premium flag. Raw time_of_day distribution: ${JSON.stringify(timeOfDayCounts)}`,
      expected: "≤60% night scenes for standard shoot economics",
      actual: `${Math.round(nightRatio * 100)}% night scenes`,
    });
  }

  return findings;
}

function check2E_SceneCountVsBeatCount(
  sceneGraph: any,
  beatSheet: any
): FeasibilityFinding[] {
  const findings: FeasibilityFinding[] = [];
  if (!sceneGraph?.scenes?.length) return findings;

  const sceneCount = sceneGraph.scenes.length;
  const beatCount = (beatSheet?.beats || beatSheet?.entries || []).length;
  if (beatCount === 0) return findings;

  if (sceneCount < beatCount) {
    findings.push({
      checkId: "2E-scene-beat-ratio",
      severity: "high",
      entityOrSystem: "scene_count",
      description: `${sceneCount} scenes for ${beatCount} beats — some beats have no script content`,
      expected: "scene_count ≥ beat_count",
      actual: `scene_count (${sceneCount}) < beat_count (${beatCount})`,
    });
  } else if (sceneCount > beatCount * 4) {
    findings.push({
      checkId: "2E-scene-beat-ratio",
      severity: "medium",
      entityOrSystem: "scene_count",
      description: `${sceneCount} scenes for ${beatCount} beats — ratio >4:1, unusually high`,
      expected: "scene_count ≤ beat_count × 4",
      actual: `${sceneCount} scenes for ${beatCount} beats`,
    });
  }

  return findings;
}

function check2F_CastSizeFeasibility(
  entities: any[],
  entityLinks: any[],
  conceptBrief: any
): FeasibilityFinding[] {
  const findings: FeasibilityFinding[] = [];

  const linkedEntityIds = [...new Set(entityLinks.map(l => l.entity_id))];
  const characters = entities.filter(e =>
    e.entity_type === "character" && linkedEntityIds.includes(e.id)
  );
  const castSize = characters.length;

  const rating = conceptBrief?.rating || conceptBrief?.target_rating || conceptBrief?.target_audience || "PG-13";
  const upperRating = rating.toUpperCase();

  let maxRecommended = 30;
  if (upperRating.includes("R")) maxRecommended = 50;
  else if (upperRating.includes("PG-13") || upperRating.includes("PG13")) maxRecommended = 30;
  else if (upperRating.includes("PG")) maxRecommended = 25;
  else if (upperRating.includes("G")) maxRecommended = 20;

  if (castSize > maxRecommended * 1.2) {
    findings.push({
      checkId: "2F-cast-size",
      severity: castSize > maxRecommended * 1.5 ? "high" : "medium",
      entityOrSystem: "cast_size",
      description: `${castSize} speaking characters exceeds ${rating} guidance (${maxRecommended} recommended). Production infeasibility risk.`,
      expected: `≤${maxRecommended} characters for ${rating}`,
      actual: `${castSize} characters`,
    });
  }

  return findings;
}

