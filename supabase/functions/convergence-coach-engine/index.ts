/**
 *enhancePrescriptionsWithLLM
 *
 * Takes the deterministic findings already generated by convergence-coach-engine,
 * then uses an LLM to transform generic "Review and align..." prescriptions into
 * scene-anchored, content-specific prescriptions with estimated GP impact.
 *
 * Architecture: LLM augments deterministic checks — it generates actionable
 * guidance for findings that were DETECTED structurally. It never invents findings.
 * Falls back to the original deterministic prescription on any error.
 */
async function enhancePrescriptionsWithLLM(
  supabaseUrl: string,
  serviceRoleKey: string,
  findings: Array<{ severity: string; description: string; checkId: string; axis: string; affectedElements?: string[] }>,
  beatSheet: any,
  sceneGraph: any,
  conceptBrief: any
): Promise<Array<{ finding: any; scene_prescription?: string; estimated_gp_impact?: number }>> {
  // Only process critical + high findings (highest leverage)
  const priorityFindings = findings.filter(f => f.severity === 'critical' || f.severity === 'high');
  if (priorityFindings.length === 0) return [];

  // Build scene context for the LLM
  const scenes = (sceneGraph?.scenes || []).slice(0, 60);
  const beats = (beatSheet?.beats || beatSheet?.entries || []).slice(0, 60);

  const sceneIndex = scenes.map((s: any, i: number) =>
    `[Scene ${i + 1}] ${s.slugline || 'Unknown'}: ${(s.content || '').slice(0, 200).replace(/\n/g, ' ').trim()}`
  ).join('\n');

  const beatIndex = beats.map((b: any, i: number) =>
    `[Beat ${b.number || i + 1}] ${b.name || b.title || 'Untitled'}: ${(b.description || '').slice(0, 150).replace(/\n/g, ' ').trim()}`
  ).join('\n');

  const findingList = priorityFindings.map((f, i) =>
    `${i + 1}. [${f.checkId}] ${f.description} (severity: ${f.severity})`
  ).join('\n');

  const systemPrompt = `You are a script consultant embedded in a production tool called IFFY. You are reviewing a screenplay's convergence diagnostic.

The deterministic convergence engine has already DETECTED the following issues. Your job is to:
1. Identify which SCENE(s) in the script each finding applies to
2. Generate a SCENE-SPECIFIC, ACTIONABLE prescription — not a generic directive
3. Estimate the GP (screenplay quality) impact on a 0–20 scale if this prescription is applied

RULES:
- You generate prescriptions for scenes that EXIST in the index. Do not hallucinate scene numbers.
- If a finding is structural/narrative-wide (e.g., missing midpoint beat, act structure drift), anchor the prescription to the most relevant specific scene and note it's structural.
- NEVER invent findings not present in the deterministic findings list.
- Format: Return valid JSON array [{"sceneSlugline": "...", "prescription": "...", "estimated_gp_impact": 0.12}, ...]
- prescription must be a concrete action: "In Scene 40, BILL receives MARCUS's threat passively — add a decision point where he secretly records the exchange"
- estimated_gp_impact is a decimal 0–20, representing expected screenplay quality improvement (e.g., 0.12 means +12 hundredths of a GP point)
- If a finding applies to no specific scene (broad structural), use sceneSlugline: "structural"
- Maximum 8 prescriptions (prioritise highest-severity findings)
- Do NOT output anything other than the JSON array`;

  const userPrompt = `CONVERGENCE FINDINGS TO PRESCRIBE:
${findingList}

SCRIPT SCENE INDEX (first 60 scenes):
${sceneIndex || 'No scenes available'}

BEAT SHEET INDEX (first 60 beats):
${beatIndex || 'No beats available'}

Generate scene-specific prescriptions for each finding above.`;

  try {
    // Use the same AI gateway pattern as dev-engine-v2
    const apiKey = Deno.env.get('LOVABLE_API_KEY') || Deno.env.get('OPENROUTER_API_KEY') || '';
    if (!apiKey) return []; // No AI key = fall back to deterministic only

    const response = await fetch('https://ai-gateway.woz-ai.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${apiKey}`,
        'x-ai-provider': 'openrouter',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: userPrompt },
        ],
        max_tokens: 4000,
        temperature: 0.2,
      }),
    });

    if (!response.ok) {
      console.error('[enhancePrescriptionsWithLLM] AI gateway returned', response.status);
      return [];
    }

    const result = await response.json() as any;
    const text = result?.choices?.[0]?.message?.content || '';
    // Extract JSON from the response
    const jsonMatch = text.match(/\[\s*\{[\s\S]*\}\s*\]/);
    if (!jsonMatch) {
      console.error('[enhancePrescriptionsWithLLM] No JSON found in response');
      return [];
    }

    const parsed = JSON.parse(jsonMatch[0]) as Array<{ sceneSlugline: string; prescription: string; estimated_gp_impact: number }>;
    return parsed.map(p => ({
      sceneSlugline: p.sceneSlugline,
      prescription: p.prescription,
      estimated_gp_impact: p.estimated_gp_impact,
    }));
  } catch (err) {
    console.error('[enhancePrescriptionsWithLLM] failed:', String(err));
    return []; // Non-blocking fallback
  }
}

/* ─────────────────────────────────────────────────────────────────
   Prescription generator (deterministic — Phase 2.1)
   ───────────────────────────────────────────────────────────────── */

function generatePrescriptions(
  nsFindings: NarrativeStructureFinding[],
  feasFindings: FeasibilityFinding[],
  blockers: Blocker[],
  fixable: FixableIssue[]
): RevisionPrescription[] {
  const prescriptions: RevisionPrescription[] = [];
  let priority = 1;

  // Critical blockers first
  for (const blocker of blockers) {
    prescriptions.push({
      axis: blocker.axis,
      priority: priority++,
      prescription: `BLOCKER: ${blocker.cannotFixWithout}`,
      whyItMatters: blocker.description,
      upstreamChange: blocker.upstreamSource,
    });
  }

  // Sort findings by severity
  const allFindings = [
    ...nsFindings.map(f => ({ ...f, axis: "narrative_structure" as const })),
    ...feasFindings.map(f => ({ ...f, axis: "feasibility" as const })),
  ];
  allFindings.sort((a, b) => {
    const sOrder = { critical: 0, high: 1, medium: 2, low: 3 };
    return (sOrder[a.severity] ?? 2) - (sOrder[b.severity] ?? 2);
  });

  for (const finding of allFindings) {
    let fixBy: string | null = null;
    if (finding.axis === "narrative_structure") {
      const f = finding as NarrativeStructureFinding;
      if (f.upstreamDoc === "story_outline" || f.divergenceType === "omission") {
        fixBy = "Regenerate beat_sheet to match story_outline structure";
      } else {
        fixBy = "Review and align beat_sheet with upstream document";
      }
    } else {
      const f = finding as FeasibilityFinding;
      if (f.checkId.startsWith("2A") || f.checkId.startsWith("2B")) {
        fixBy = "Rerun entity linking pipeline to sync entity counts";
      } else {
        fixBy = "Review feasibility constraints in concept brief or scene graph";
      }
    }

    prescriptions.push({
      axis: finding.axis,
      priority: priority++,
      prescription: fixBy,
      whyItMatters: finding.description,
      upstreamChange: null,
    });
  }

  return prescriptions;
}

/* ─────────────────────────────────────────────────────────────────
   Main handler
   ───────────────────────────────────────────────────────────────── */

serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { projectId, axes } = await req.json();

    if (!projectId) {
      return new Response(JSON.stringify({ error: "projectId is required" }), {
        status: 400,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    // Service role client — reads all data
    const supabase = createClient(
      Deno.env.get("SUPABASE_URL") ?? "",
      Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
    );

    // ── Fetch all canonical data ──
    const [
      beatSheetVersion,
      storyOutlineVersion,
      treatmentVersion,
      conceptBriefVersion,
      sceneGraph,
      entities,
      entityLinks,
    ] = await Promise.all([
      fetchLatestDocVersion(supabase, projectId, "beat_sheet"),
      fetchLatestDocVersion(supabase, projectId, "story_outline"),
      fetchLatestDocVersion(supabase, projectId, "treatment"),
      fetchLatestDocVersion(supabase, projectId, "concept_brief"),
      fetchSceneGraph(
        Deno.env.get("SUPABASE_URL") ?? "",
        Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
        projectId
      ),
      fetchNarrativeEntities(
        Deno.env.get("SUPABASE_URL") ?? "",
        Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
        projectId
      ),
      fetchEntityLinks(
        Deno.env.get("SUPABASE_URL") ?? "",
        Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
        projectId
      ),
    ]);

    const beatSheet = beatSheetVersion ? extractDocContent(beatSheetVersion) : null;
    const storyOutline = storyOutlineVersion ? extractDocContent(storyOutlineVersion) : null;
    const treatment = treatmentVersion ? extractDocContent(treatmentVersion) : null;
    const conceptBrief = conceptBriefVersion ? extractDocContent(conceptBriefVersion) : null;

    // ── Axis 1: Narrative Structure ──
    const nsFindings: NarrativeStructureFinding[] = [];
    let nsStatus: "converged" | "diverged" | "unknown" = "unknown";
    let nsScore = 1.0;

    const runNarrativeStructure = !axes || axes.includes("narrative_structure");

    if (runNarrativeStructure) {
      if (!beatSheet || !storyOutline || !treatment) {
        nsStatus = "unknown";
        nsScore = 0;
      } else {
        const finding1A = check1A_BeatCountConsistency(storyOutline, beatSheet);
        if (finding1A) nsFindings.push(finding1A);

        nsFindings.push(...check1B_BeatNameConsistency(storyOutline, beatSheet));
        nsFindings.push(...check1C_ActStructurePreservation(treatment, beatSheet));
        nsFindings.push(...check1D_CharacterArcConsistency(conceptBrief, beatSheet));

        const finding1E = check1E_MidpointConvergence(treatment, beatSheet);
        if (finding1E) nsFindings.push(finding1E);

        nsStatus = nsFindings.length === 0 ? "converged" : "diverged";
        nsScore = scoreFindings(nsFindings);
      }
    }

    // ── Axis 2: Feasibility ──
    const feasFindings: FeasibilityFinding[] = [];
    let feasStatus: "converged" | "diverged" | "unknown" = "unknown";
    let feasScore = 1.0;

    const runFeasibility = !axes || axes.includes("feasibility");

    if (runFeasibility) {
      // Run whatever checks we can with available data.
      // scene_graph may be unavailable due to RLS even with service role — degrade gracefully.
      feasFindings.push(...check2A_EntityPresenceConsistency(entities, entityLinks));
      feasFindings.push(...check2F_CastSizeFeasibility(entities, entityLinks, conceptBrief));

      if (sceneGraph?.scenes?.length) {
        feasFindings.push(...await check2B_viaREST(
          Deno.env.get("SUPABASE_URL") ?? "",
          Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? "",
          projectId
        ));
        feasFindings.push(...check2C_LocationFeasibility(sceneGraph));
        feasFindings.push(...check2D_DayNightDistribution(sceneGraph));
        feasFindings.push(...check2E_SceneCountVsBeatCount(sceneGraph, beatSheet));
      }

      if (entities.length === 0 && !sceneGraph?.scenes?.length) {
        feasStatus = "unknown";
        feasScore = 0;
      } else {
        feasStatus = feasFindings.length === 0 ? "converged" : "diverged";
        feasScore = scoreFindings(feasFindings);
      }
    }

    // ── Blockers and fixable ──
    const blockers: Blocker[] = [];
    const fixable: FixableIssue[] = [];

    for (const f of nsFindings) {
      if (f.severity === "critical" && f.upstreamDoc !== "beat_sheet") {
        blockers.push({
          axis: "narrative_structure",
          description: f.description,
          upstreamSource: f.upstreamDoc,
          cannotFixWithout: `${f.upstreamDoc} must be corrected before beat_sheet can converge`,
        });
      } else {
        fixable.push({
          axis: "narrative_structure",
          description: f.description,
          fixBy: "regenerate_beat_sheet",
          estimatedEffort: "medium",
        });
      }
    }

    for (const f of feasFindings) {
      if (f.checkId === "2B-character-population") {
        fixable.push({
          axis: "feasibility",
          description: f.description,
          fixBy: "rerun_entity_links",
          estimatedEffort: "medium",
        });
      } else {
        fixable.push({
          axis: "feasibility",
          description: f.description,
          fixBy: "regenerate_beat_sheet",
          estimatedEffort: "medium",
        });
      }
    }

    // ── Convergence trajectory ──
    const criticalCount = [...nsFindings, ...feasFindings].filter(f => f.severity === "critical").length;
    let trend: "improving" | "stable" | "degrading" | "unknown" = "unknown";
    let trendReason = "Insufficient historical data for trend analysis";

    if (nsStatus === "converged" && feasStatus === "converged") {
      trend = "stable";
      trendReason = "All convergence axes are currently converged";
    } else if (criticalCount >= 2) {
      trend = "degrading";
      trendReason = `${criticalCount} critical findings indicate active narrative drift`;
    } else if (nsFindings.length > 0 || feasFindings.length > 0) {
      trend = "degrading";
      trendReason = `${nsFindings.length + feasFindings.length} convergence issues found — drift is occurring`;
    } else {
      trend = "stable";
      trendReason = "No convergence issues detected";
    }

    // ── Prescriptions ──
    const revisionPrescriptions = generatePrescriptions(nsFindings, feasFindings, blockers, fixable);

    // ── Phase 2.2: LLM scene-anchored prescription enhancement ──
    // Run in background — does not block the diagnostic response.
    // Augments deterministic prescriptions with scene-specific actionable guidance + GP impact.
    if (Deno.env.get('LOVABLE_API_KEY') || Deno.env.get('OPENROUTER_API_KEY')) {
      try {
        const allFindings = [
          ...nsFindings.map(f => ({ ...f, axis: 'narrative_structure' as const, affectedElements: f.affectedElements || [] })),
          ...feasFindings.map(f => ({ ...f, axis: 'feasibility' as const, affectedElements: [f.entityOrSystem] })),
        ];
        const llmEnhanced = await enhancePrescriptionsWithLLM(
          Deno.env.get('SUPABASE_URL') ?? '',
          Deno.env.get('SUPABASE_SERVICE_ROLE_KEY') ?? '',
          allFindings,
          beatSheet,
          sceneGraph,
          conceptBrief
        );

        // Merge LLM prescriptions into revisionPrescriptions
        if (llmEnhanced.length > 0) {
          for (const enhanced of llmEnhanced) {
            // Find the prescription with the matching finding (by priority index)
            const idx = revisionPrescriptions.findIndex(p => {
              // Match by severity and position in list
              return true; // Already ordered by severity in generatePrescriptions
            });
            // Attach scene_prescription to highest-severity unmatched prescriptions
            // Simple merge: pair in order (findings are sorted by severity already)
          }
          // Simpler approach: inject scene_prescription into prescriptions by position
          for (let i = 0; i < revisionPrescriptions.length && i < llmEnhanced.length; i++) {
            revisionPrescriptions[i].scene_prescription = llmEnhanced[i].prescription;
            revisionPrescriptions[i].estimated_gp_impact = llmEnhanced[i].estimated_gp_impact;
          }
        }
      } catch (e) {
        console.error('[convergence-coach] LLM enhancement failed (non-blocking):', String(e));
      }
    }

    // ── Assemble response ──
    const diagnostic: ConvergenceDiagnostic = {
      projectId,
      generatedAt: new Date().toISOString(),
      narrativeStructureAxis: {
        status: nsStatus,
        score: Math.round(nsScore * 100) / 100,
        findings: nsFindings,
      },
      feasibilityAxis: {
        status: feasStatus,
        score: Math.round(feasScore * 100) / 100,
        findings: feasFindings,
      },
      convergenceTrajectory: {
        trend,
        trendReason,
        blockers,
        fixable,
      },
      revisionPrescriptions,
    };

    return new Response(JSON.stringify(diagnostic), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });

  } catch (err: any) {
    return new Response(JSON.stringify({ error: err.message }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
