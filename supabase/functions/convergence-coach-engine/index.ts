/** Extract uppercase normalised key entities from a text string. */
function extractKeyEntities(text: string): string[] {
  if (!text) return [];
  const words = text.toUpperCase().match(/[A-Z][A-Z0-9]+/g) || [];
  // Filter out stopwords
  const stop = new Set(["THE", "AND", "FOR", "WITH", "FROM", "INTO", "INTO", "IN", "ON", "AT", "TO", "OF", "A", "AN", "IS", "ARE", "WAS", "WERE", "BE", "BY", "AS", "THAT", "THIS", "BUT", "OR", "NOT", "THEY", "THEM", "HAVE", "HAS", "HAD", "ITS", "IT", "HE", "SHE", "WE", "YOU", "HIS", "HER", "THEIR", "THAN", "THEN", "SO", "WHEN", "WHERE", "WHICH", "WHO", "WHAT", "WHY", "HOW", "ALL", "EACH", "BOTH", "FEW", "MORE", "MOST", "OTHER", "SOME", "SUCH", "ONLY", "OWN", "SAME", "VERY", "JUST", "ALSO", "NOW", "HERE", "THERE", "CAN", "WILL", "JUST", "JUST"]);
  return [...new Set(words.filter(w => w.length > 2 && !stop.has(w)))];
}

/** Count occurrences of each string in an array. */
function countOccurrences(arr: string[]): Record<string, number> {
  const counts: Record<string, number> = {};
  for (const v of arr) {
    counts[v] = (counts[v] || 0) + 1;
  }
  return counts;
}

/** Score a findings array — 1.0 = no findings, decreases with severity. */
function scoreFindings(findings: Array<{ severity: string }>): number {
  if (findings.length === 0) return 1.0;
  const weight: Record<string, number> = { critical: 0.3, high: 0.6, medium: 0.8, low: 0.95 };
  const total = findings.reduce((acc, f) => acc + (weight[f.severity] ?? 0.7), 0);
  return Math.max(0, 1 - (total / findings.length) * 0.1);
}

/** Extract structured content from a project_document_version.
 * meta_json is the primary store (JSONB).
 * plaintext also contains JSON for reverse-engineered docs.
 */
function extractDocContent(version: any): any {
  if (!version) return null;
  // meta_json is a JSONB field — supabase returns it as an object already
  if (version.meta_json && typeof version.meta_json === 'object') {
    return version.meta_json;
  }
  // plaintext contains JSON for reverse-engineered documents (e.g. beat_sheet, story_outline)
  if (version.plaintext) {
    try {
      return JSON.parse(version.plaintext);
    } catch {
      return null;
    }
  }
  return null;
}

/** Fetch latest is_current version for a doc type in a project. */
async function fetchLatestDocVersion(
  supabase: any,
  projectId: string,
  docType: string
): Promise<any | null> {
  // 1. Get the project_document record
  const { data: doc } = await supabase
    .from("project_documents")
    .select("id")
    .eq("project_id", projectId)
    .eq("doc_type", docType)
    .maybeSingle();

  if (!doc) return null;

  // 2. Get the latest is_current version
  const { data: version } = await supabase
    .from("project_document_versions")
    .select("*")
    .eq("document_id", doc.id)
    .eq("is_current", true)
    .maybeSingle();

  return version || null;
}

/** Fetch latest scene graph for a project.
 * Uses the Supabase REST API via direct fetch (bypasses potential Edge Runtime supabase client quirks).
 */
async function fetchSceneGraph(supabaseUrl: string, serviceRoleKey: string, projectId: string): Promise<any | null> {
  try {
    const headers = {
      "apikey": serviceRoleKey,
      "Authorization": `Bearer ${serviceRoleKey}`,
      "Content-Type": "application/json",
    };

    // Get all scenes for this project
    const scenesUrl = `${supabaseUrl}/rest/v1/scene_graph_scenes?project_id=eq.${projectId}&select=id,scene_key&order=scene_key.asc`;
    const scenesRes = await fetch(scenesUrl, { headers });
    if (!scenesRes.ok) return null;
    const scenes: Array<{ id: string; scene_key: string }> = await scenesRes.json();
    if (!scenes || scenes.length === 0) return null;

    const sceneIds = scenes.map(s => s.id);

    // Get all draft versions for these scenes
    // Filter by status='draft' and get latest version per scene using max(version_number)
    const versionsUrl = `${supabaseUrl}/rest/v1/scene_graph_versions?project_id=eq.${projectId}&status=eq.draft&scene_id=in.(${sceneIds.join(",")})&select=scene_id,slugline,location,time_of_day,characters_present,content,version_number&order=version_number.desc`;
    const versionsRes = await fetch(versionsUrl, { headers });
    if (!versionsRes.ok) return null;
    const versions: Array<any> = await versionsRes.json();
    if (!versions || versions.length === 0) return null;

    // Deduplicate: keep first (highest version_number) per scene_id
    const versionMap = new Map<string, any>();
    for (const v of versions) {
      if (!versionMap.has(v.scene_id)) {
        versionMap.set(v.scene_id, v);
      }
    }

    const mergedScenes = scenes
      .map(s => {
        const v = versionMap.get(s.id);
        return {
          scene_key: s.scene_key,
          slugline: v?.slugline || "",
          location: v?.location || "",
          time_of_day: v?.time_of_day || "",
          characters_present: v?.characters_present || [],
          content: v?.content || "",
        };
      })
      .filter(s => s.slugline || s.content);

    return { scenes: mergedScenes };
  } catch {
    return null;
  }
}

